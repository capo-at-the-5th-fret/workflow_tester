#pragma once

namespace math
{
    inline int add(int x, int y)
    {
        return x + y;
    }

    inline int subtract(int x, int y)
    {
        return x - y;
    }

    inline int multiply(int x, int y)
    {
        return x * y;
    }

    inline int divide(int x, int y)
    {
        return x / y;
    }
}
